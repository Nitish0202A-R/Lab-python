a = 10
b = 11.5
c = 2.0 + 5j

print("a is Type of ", type(a))   # <class 'int'>
print("b is Type of ", type(b))   # <class 'float'>
print("c is Type of ", type(c))   # <class 'complex'>

l = [1, 2, 3, 4, 5]
print("l is type of", type(l))    # <class 'list'>

t = (1, 2, 3, 4, 5)
print("t is of type", type(t))    # <class 'tuple'>

student_id = {111, 112, 113, 114, 115}
print("student_id is Type of ", type(student_id))   # <class 'set'>

example = {1: "A", 2: "B", 3: "C", 4: "D"}
print("capital is Type of ", type(example))         # <class 'dict'>

message = "Good Morning"
